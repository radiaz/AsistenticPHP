<h3>OBJETIVOS DEL SEMILLERO.</h3>

<h4>GENERALES.</h4>
<ul>
<li>Proporcionar herramientas de software (Objetos Virtuales De Aprendizaje OVA, Actividades Educativas Digitales y Ambientes Virtuales De Aprendizaje - AVA)  que apoyen el proceso de enseñanza y aprendizaje en el aula a la comunidad educativa del ITFIP Institución de educación superior Espinal Tolima.</li> 
<li>Generar conciencia investigativa en la búsqueda de la calidad educativa y proyección social a través de las tecnologías de la información y la comunicación TIC desde la perspectiva de las OVA.</li>
</ul> 
 
<h4>ESPECIFICOS.</h4>
<ul>
<li>Desarrollar nuevos ambientes de aprendizaje soportados en las TIC.</li>
<li>Diseñar y desarrollar objetos virtuales de aprendizaje OVA para apoyar el proceso de enseñanza y de aprendizaje de las áreas del conocimiento de las diferentes carreras técnicas y tecnológicas que ofrece la institución.</li>
<li>Diseñar y desarrollar Ambientes Virtuales de Aprendizaje – AVA para apoyar el proceso de enseñanza y de aprendizaje de las áreas del conocimiento de las diferentes carreras técnicas y tecnológicas que ofrece la institución.</li>
<li>Fomentar el uso de las TIC  para mejorar su estilo de vida personal y empresarial.</li>
<li>Fomentar la investigación en los estudiantes como fuente de enriquecimiento para su vida profesional.</li>
</ul>
 
