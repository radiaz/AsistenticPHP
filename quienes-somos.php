<h3>MISIÓN DEL SEMILLERO.</h3>
<p>Promover en la comunidad educativa del ITFIP  la construcción y utilización de herramientas tecnológicas, Objetos Virtuales De Aprendizaje OVA, Actividades Educativas Digitales y Ambientes Virtuales De Aprendizaje - AVA  brindando  a la comunidad una estrategia innovadora para soportar el proceso de enseñanza – aprendizaje acorde con  las nuevas tecnologías de la información y la comunicación TIC.</p>

<h3>VISIÓN DEL SEMILLERO</h3>
<p>En el 2015 ser reconocidos a nivel regional, nacional e internacional como un grupo de investigación integrados por profesionales líderes en el diseño, desarrollo e implementación de Herramientas Tecnológicas  sustentadas por la utilización de las nuevas tecnologías de la información y la comunicación TIC haciendo uso de la multimedia (animaciones flash, videos, sonidos, textos, graficas, fotografías) y la hipermedia para crear ambientes educativos interesantes e interactivos para los estudiantes del ITFIP  en formato Web que contribuyan con el proceso de enseñanza – aprendizaje de las áreas del conocimiento de las diferentes carreras técnicas y tecnológicas que ofrece la institución.</p>

